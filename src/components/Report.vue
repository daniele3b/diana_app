        let month=data.getMonth()+1
        let day=data.getDate()
        let year=data.getFullYear()
        
        if(month<10)
          month='0'+month
        if(day<10)
          day='0'+day

          axios({
            method: 'get',
            url: 'http://localhost:8081/report/filter/date/'+year+'-'+month+'-'+day,
            headers: {
              "x-diana-auth-token": localStorage.token
            }
