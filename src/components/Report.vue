export default {
    name:'Report',
    data() {
        return {
         reports:[],
         adding:false,
         address:'',
         category:'',
         description:'',
         addresscheck:false,
         categorycheck:false,
         descriptioncheck:false,
         zoomed:false,
         CF:'',
         date:'',
         status:'',
